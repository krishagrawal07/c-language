#include<stdio.h>
int main(){
//switch = an alternative to using many if-else statements more efficient w/ fixed integer values
    int dayofweek = 1;

    switch(dayofweek){
          case 1 :
          printf("It is monday");
          break;
          
          case 2 :
          printf("It is tuesday");
          break;
    }

return 0;

}